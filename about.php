<h3>The vision</h3>

<p>Artmoney is a tool for investigating the nature of human value. The method is to study, evaluate, conclude, act, reflect, and study again. It is an ongoing cycle that evolves as a wheel of transformation. The artmoney community itself is an ever-changing organism. We hope that artmoney can contribute to the discovery of beauty in general and individual purpose in specific.<br><br>

Artmoney is a “down to earth art project” driven by concrete results and utopian visions.<br><br>

Artmoney is based on a decentralized management in regards to action and relies on chaos as a source of inspiration. Each artmoney artist is regarded a "bank manager" who has a mandate to shape the evolution by their own dreams and actions. The artmoney head office simply holds the strings of change in a central portal and channel incoming influences by displaying some of the visual results on this site for all to view. Many more artmoney circulate around the world as physical art objects in search of appreciation and understanding. Artmoney reflects the body of a collective human effort and may as such be considered as "soft cash."<br><br>

Your vision for artmoney may be completely different from mine. Artmoney represents as many visions as there are artmoney artists. My vision is to realize some of the utopian dreams born out of art:</p>

<ul  style="list-style-type:square">
<li>Freedom from economic slavery to live the life we wish for</li>

<li>A global network of shops that accepts artmoney</li>

<li>A hand made currency that offers poor people the opportunity to paint their money on the spot and buy stuff</li>

<li>To slow down time</li>

<li>To steal the attention away from war and corruption and embrace beauty instead of greed
To level with each other in spite of differences in status, culture, religion, or political standpoint

<li>To see and share and learn</li>

<li>To introduce function to art and art to living</li></ul>


