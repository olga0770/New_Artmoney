<h3>The history of Artmoney</h3>
<p>It all began in December 1997 when I painted the first artmoney. The idea was clear from the beginning. I wanted to create a "personal currency" that would enable me to draw my own money and free me from economic slavery. What I had not dared to hope then, was that artmoney could become a currency for people worldwide.<br><br>
From the start I had luck with using artmoney in stores. I also found that the small works of art were easy to exchange (sell) into “ordinary” money if I needed it.<br><br>
In 1998, artist, Flemming Vincent, joined the project. We founded the "Bank of International Artmoney" (BIAM) and made the first web site, so artists from near and far to join the "bank." The Internet had just become available to the general public, and it seemed natural to put the project out to the world community so everyone could make their own money.<br><br>
In 2001, I traveled half a year on a motorcycle through North America and paid the trip with artmoney, I painted along the way. The trip demonstrated artmoney as an excellent travel currency! Jesper Jargil film began shooting a documentary about the project.<br><br>
In 2003, BIAM has grown to more than 100 artists from Denmark, the United States and Canada. We were invited to exhibit in the 5000 square meter showroom “Oeksnehallen,” downtown, Copenhagen. The exhibition was opened by former CEO of the National Bank of Denmark, Erik Hoffmeyer. It received massive press coverage and set record attendance despite a historic snowfall.<br><br>
Oeksnehallen was the beginning of a true adventure.<br><br>
With the help of Peter Rodenberg from InleadMedia, the project created a new site that could sell the artists artmoney online. BIAM was registered as a company. We got busy and Vincent resigned from the board to devote his time to his own art. Soon the auction house, “Lauritz.com,” bought 49% of BIAM by paying for the development of yet another new site.<br><br>
In 2005, I was charged by the “National Financial Advisory Board” for the illegal use of the word "bank." The penalty was up to four months in prison. After a long trial I got off with a fine of Danish Kroner 2000 and lost the right to the use of the word “Bank.” There was no law forbidding us to paint our own money, so we continued doing that. The court case received much media attention, which meant that many new artists joined. Soon BIAM reached 1000 members.<br><br>
In 2012 Lauritz.com pulled out of BIAM, since we did not share the same views on the purpose of artmoney.<br><br>
In 2014 “Korpus Design” developed a new site where artists could upload their own artmoney and sell them directly from home without commission. Simultaneously, shops and collectors became able to sell artmoney online, and the movement of the alternative currency had reached a complete cyclus.
Between the lines the story of artmoney holds thousands of adventures experienced by the many people involved. And the story continues...</p>

