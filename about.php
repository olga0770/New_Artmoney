<h3>What is Artmoney?</h3>

<p>The basic idea of artmoney is, that artists produce a hand-made currency at a fixed value that can be spend at regular shops.<br><br>

The concept of exchanging art for goods and services is as old as humankind and the value of art is recognized by all people. Artmoney simply introduces a system that unites format and value to make exchange easy, fun and beautiful.<br><br>

Artmoney holds the format 12x18 cm and the value of Danish Kroner 200. This applies to all artists at all social and professional levels from all countries. When it comes to artmoney, all people are worth the same.<br><br>
Artists make their own money.<br>
Companies accept artmoney as payment.<br>
Collectors buy artmoney as inexpensive, original art.<br><br>

Check the value of artmoney in your local currency (Danish Kroner 200) with <a href="https://finance.google.com/finance/converter">CURRENCY CONVERTER.</a>
